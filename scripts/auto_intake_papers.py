def _is_author_line(line):
    """Detect if a line looks like an author list (not a title)."""
    # Pipe-separated names: "Kevin Bauer | Lena Liebich | Oliver Hinz"
    if '|' in line and re.search(r'[A-Z][a-z]+\s+[A-Z]', line):
        parts = [p.strip() for p in line.split('|')]
        name_like = sum(1 for p in parts if re.match(r'^[A-Z][a-z]+', p))
        if name_like >= 2:
            return True
    # Semicolon-separated: "Andersen, Torben M.; Svarer, Michael"
    if ';' in line and re.match(r'^[A-Z][a-z]+,\s+[A-Z]', line):
        return True
    # Comma-separated names: "Dennis Herhausen, Stephan Ludwig, Dhruv Grewal"
    if re.match(r'^[A-Z][a-z]+\s+[A-Z][a-z]+,\s+[A-Z][a-z]+\s+[A-Z]', line):
        return True
    # ALL CAPS short names: "RICHARD  H.  THALER" (tab-separated SSRN style)
    if re.match(r'^[A-Z]{2,}\s+[A-Z]\.?\s+[A-Z]{2,}$', line):
        return True
    # University/affiliation line
    if re.match(r'^(Booth\s|Harvard|MIT|Stanford|University|Department|School|Institute|Center|Centre)', line, re.IGNORECASE):
        return True
    return False


def _is_journal_header(line):
    """Detect if a line is a journal/publisher header (not a title)."""
    journal_headers = [
        r'^PLoS\s', r'^Nature\s', r'^Science\s', r'^The Lancet',
        r'^www\.\w+\.\w+', r'^https?://',
        r'^\d{4}\s', r'^Volume\s+\d', r'^Vol\.\s*\d',
        r'^Open access', r'^freely available',
        r'^August|^September|^October|^November|^December|^January|^February|^March|^April|^May|^June|^July',
        r'^Essay$', r'^\d+$',  # Page numbers, section labels
        r'^Provided in Cooperation', r'^Suggested Citation',
        r'^This Version is available', r'^Standard-Nutzungsbedingungen',
        r'^CESifo Working Paper', r'^IZA Discussion Paper',
        r'^SAFE Working Paper', r'^Discussion Paper',
    ]
    for pattern in journal_headers:
        if re.match(pattern, line, re.IGNORECASE):
            return True
    return False


def _title_from_filename(filepath):
    """Extract a clean title from filename (often the best source for BEA Lab uploads)."""
    name = filepath.stem
    # Remove common prefixes/suffixes
    name = re.sub(r'^\[APE\]_?', '', name)
    name = re.sub(r'SSRN-id\d+$', '', name, flags=re.IGNORECASE)
    name = re.sub(r'v\d+$', '', name, flags=re.IGNORECASE)
    # Clean separators
    name = re.sub(r'[_+]', ' ', name)
    name = re.sub(r'\s+', ' ', name).strip()
    # Skip pure numeric filenames (e.g. "2302.08854", "555973581")
    if re.match(r'^[\d.]+$', name):
        return None
    # Skip very short names
    if len(name) < 5:
        return None
    return name


def extract_metadata_from_txt(filepath):
    """
    Extract metadata from a text file (BEA Lab upload).

    Strategy: Read first ~80 lines and look for structured metadata patterns:
    - DOI patterns (10.xxxx/...)
    - Title (from filename first, validated/improved from text)
    - Authors (lines with comma-separated names, pipe-separated, etc.)
    - Journal names
    - Year (4-digit number)
    """
    try:
        with open(filepath, 'r', encoding='utf-8', errors='replace') as f:
            raw_lines = f.readlines()[:80]
    except Exception as e:
        return {'error': str(e)}

    if not raw_lines:
        return {'error': 'Empty file'}

    # Clean lines: strip whitespace and replace tabs with spaces
    lines = [re.sub(r'\t+', ' ', l).strip() for l in raw_lines]
    # Remove zero-width chars and other artifacts
    lines = [re.sub(r'[\x00-\x08\x0b\x0c\x0e-\x1f]', '', l) for l in lines]

    metadata = {
        'title': None,
        'authors': [],
        'year': None,
        'doi': None,
        'journal': None,
        'source_file': str(filepath.relative_to(ROOT)),
        'extraction_method': 'txt_header_parse',
    }

    full_header = '\n'.join(lines[:50])

    # --- DOI ---
    doi_match = re.search(r'(10\.\d{4,}/[^\s,;]+)', full_header)
    if doi_match:
        doi = doi_match.group(1).rstrip('.')
        metadata['doi'] = doi

    # --- Year ---
    for line in lines[:30]:
        year_match = re.search(r'\b(19[5-9]\d|20[0-2]\d)\b', line)
        if year_match:
            candidate = int(year_match.group(1))
            if 1950 <= candidate <= 2027:
                metadata['year'] = candidate
                break

    # --- APE Paper special handling ---
    filename = filepath.name
    if filename.startswith('[APE]'):
        title_from_name = filename.replace('[APE]_', '').replace('.txt', '')
        title_from_name = title_from_name.replace('_', ' ').strip()
        title_from_name = re.sub(r'\s+', ' ', title_from_name)
        metadata['title'] = title_from_name
        metadata['source_type'] = 'ape_working_paper'
        for line in lines[:10]:
            if re.match(r'^[A-Z][a-z]+ [A-Z]', line) and ',' not in line[:30]:
                metadata['authors'].append(line.split(',')[0].strip())
        return metadata

    # --- Authors (extract before title to use for filtering) ---
    for i, line in enumerate(lines[:20]):
        if _is_author_line(line):
            if '|' in line:
                parts = [p.strip() for p in line.split('|') if p.strip()]
                # Filter out URLs and journal names
                metadata['authors'] = [p for p in parts if re.match(r'^[A-Z][a-z]', p) and 'www' not in p.lower()]
            elif ';' in line:
                parts = re.split(r';\s*', line)
                metadata['authors'] = [p.strip() for p in parts if p.strip()]
            elif ',' in line:
                parts = re.split(r',\s*(?:and\s+)?', line)
                metadata['authors'] = [p.strip() for p in parts if p.strip() and len(p.strip()) > 3]
            if metadata['authors']:
                break

    # --- Title from filename (primary source for BEA Lab uploads) ---
    filename_title = _title_from_filename(filepath)

    # --- Title from text content ---
    text_title = None
    title_candidates = []
    for i, line in enumerate(lines[:20]):
        if not line or len(line) < 10:
            continue
        # Skip known non-title patterns
        if re.match(r'(https?://|DOI|doi:|10\.\d|©|Version of Record|document|Link to|General rights|Copyright|E-mail|Phone|Electronic copy|SSRN|Abstract:|Keywords:|JEL|Manuscript_|ssrn\.com)', line, re.IGNORECASE):
            continue
        if re.match(r'^(VU Research Portal|published in|citation for|Download date|Working Paper|NBER|CEPR|Terms of use|Documents in)', line, re.IGNORECASE):
            continue
        if _is_author_line(line):
            continue
        if _is_journal_header(line):
            continue
        # Clean trailing artifacts (e.g. "ARCHITECTURE0" -> "ARCHITECTURE")
        cleaned = re.sub(r'\d+\s*$', '', line).strip()
        if cleaned and len(cleaned) >= 10:
            title_candidates.append((i, cleaned))

    if title_candidates:
        # Pick the first substantial candidate
        for idx, candidate in title_candidates:
            if len(candidate) > 15:
                text_title = candidate
                break
        if not text_title and title_candidates:
            text_title = title_candidates[0][1]

    # --- Choose best title ---
    # Quality check on text title
    text_title_ok = False
    if text_title:
        # Title must start with uppercase letter (real titles always do)
        starts_upper = bool(re.match(r'^[A-Z"\']', text_title))
        # Not garbage patterns
        not_garbage = not bool(re.search(r'^[A-Z]{2,}\s+[A-Z]{2,}$|^\d|^P$', text_title))
        # Contains at least some real words
        has_words = len(text_title.split()) >= 2
        # No broken PDF artifacts (ligatures etc.)
        no_artifacts = 'ﬁ' not in text_title and 'ﬂ' not in text_title
        text_title_ok = starts_upper and not_garbage and has_words and no_artifacts

    if filename_title and text_title and text_title_ok:
        text_words = len(text_title.split())
        fn_words = len(filename_title.split())
        if text_words >= 3 and text_words > fn_words:
            metadata['title'] = text_title
        else:
            metadata['title'] = filename_title
    elif text_title and text_title_ok:
        metadata['title'] = text_title
    elif filename_title:
        metadata['title'] = filename_title
    elif text_title:
        # Use text title even if not perfect - better than nothing
        metadata['title'] = text_title
    else:
        # Last resort: use raw filename
        name = filepath.stem
        name = re.sub(r'[_]', ' ', name)
        name = re.sub(r'\s+', ' ', name).strip()
        metadata['title'] = name

    # --- Journal ---
    journal_patterns = [
        r'(?:published in|journal of|Journal of)\s+(.+)',
        r'^(Journal of \w[\w\s]+)',
        r'^(American Economic Review|Quarterly Journal of Economics|Econometrica|Review of Economic Studies)',
    ]
    for line in lines[:20]:
        for pattern in journal_patterns:
            m = re.search(pattern, line, re.IGNORECASE)
            if m:
                metadata['journal'] = m.group(1).strip()
                break
        if metadata['journal']:
            break

    return metadata


def extract_metadata_from_pdf(filepath):
    """
    Extract metadata from PDF filename (actual text extraction requires PyMuPDF).
    Falls back to filename-based extraction.
    """
    metadata = {
        'title': None,
        'authors': [],
        'year': None,
        'doi': None,
        'journal': None,
        'source_file': str(filepath.relative_to(ROOT)),
        'extraction_method': 'pdf_filename_parse',
    }

    name = filepath.stem
    # Try to find year in filename
    year_match = re.search(r'(19[5-9]\d|20[0-2]\d)', name)
    if year_match:
        metadata['year'] = int(year_match.group(1))

    # Use improved filename title extraction
    filename_title = _title_from_filename(filepath)
    if filename_title:
        metadata['title'] = filename_title
    else:
        # Pure numeric or very short filename
        metadata['title'] = name

    # Check if companion .txt exists (already extracted)
    txt_path = filepath.with_suffix('.txt')
    if txt_path.exists():
        txt_meta = extract_metadata_from_txt(txt_path)
        # Merge: prefer txt metadata where available
        for key in ['doi', 'journal', 'authors', 'year']:
            if txt_meta.get(key) and (not metadata.get(key) or key == 'authors' and not metadata[key]):
                metadata[key] = txt_meta[key]
        if txt_meta.get('title') and len(txt_meta['title']) > len(metadata.get('title', '')):
            metadata['title'] = txt_meta['title']
        metadata['has_txt_companion'] = True
        metadata['extraction_method'] = 'pdf_with_txt_companion'

    return metadata


def generate_bibtex_key(metadata):
    """Generate a canonical BibTeX key: nachnamejahrkurzwort.

    Delegates to bibtex_key_generator.generate_canonical_key() (SSOT).
    This wrapper adapts the metadata-dict interface used by auto_intake.
    """
    authors = metadata.get('authors', [])
    author_str = authors[0] if authors else ''
    year = metadata.get('year') or '0000'
    title = metadata.get('title', '')

    # ArXiv fallback: if filename is numeric and no author found
    if not author_str:
        source_file = metadata.get('source_file', '')
        source_stem = Path(source_file).stem if source_file else ''
        if re.match(r'^[\d.]+$', source_stem):
            arxiv_id = source_stem.replace('.', '')
            return f"arxiv{year}{arxiv_id}"

    return _generate_canonical_key(author_str, year, title)


def create_bibtex_entry(key, metadata):
    """Create a BibTeX entry string."""
    title = metadata.get('title', 'Unknown Title')
    authors = metadata.get('authors', [])
    year = metadata.get('year', '')
    doi = metadata.get('doi', '')
    journal = metadata.get('journal', '')

    # Format authors for BibTeX
    if authors:
        author_str = ' and '.join(authors)
    else:
        author_str = 'Unknown'

    source_type = metadata.get('source_type', '')
    if source_type == 'ape_working_paper':
        entry_type = 'unpublished'
    elif journal:
        entry_type = 'article'
    else:
        entry_type = 'misc'

    lines = [f"@{entry_type}{{{key},"]
    lines.append(f"  title = {{{title}}},")
    lines.append(f"  author = {{{author_str}}},")
    if year:
        lines.append(f"  year = {{{year}}},")
    if journal:
        lines.append(f"  journal = {{{journal}}},")
    if doi:
        lines.append(f"  doi = {{{doi}}},")

    # EBF fields
    lines.append(f"  use_for = {{LIT-O}},")
    lines.append(f"  evidence_tier = {{3}},")
    lines.append(f"  notes = {{Auto-intake from BEA Lab upload: {metadata.get('source_file', '')}}},")
    lines.append(f"  session_ref = {{auto-intake-{datetime.now().strftime('%Y-%m-%d')}}},")
    lines.append("}")

    return '\n'.join(lines)


