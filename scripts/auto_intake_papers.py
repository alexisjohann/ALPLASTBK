def process_file(filepath, existing_keys, existing_yamls, doi_to_key, title_to_key, manifest, queue_data, dry_run=False):
    """Process a single file. Returns (status, key, metadata).
    status: 'created', 'linked', 'skipped', 'error'
    """
    filename = filepath.name

    if should_skip(filename):
        manifest['skipped'][filename] = {
            'reason': 'matched_skip_pattern',
            'date': datetime.now().strftime('%Y-%m-%d'),
        }
        return 'skipped', None, None

    # Extract metadata
    ext = filepath.suffix.lower()
    if ext == '.txt':
        metadata = extract_metadata_from_txt(filepath)
    elif ext == '.pdf':
        metadata = extract_metadata_from_pdf(filepath)
    elif ext == '.md':
        metadata = extract_metadata_from_txt(filepath)  # Same parser works
    else:
        return 'skipped', None, None

    if 'error' in metadata:
        print(f"  ERROR: {metadata['error']}")
        manifest['skipped'][filename] = {
            'reason': f"extraction_error: {metadata['error']}",
            'date': datetime.now().strftime('%Y-%m-%d'),
        }
        return 'error', None, None

    # Check if paper already exists by DOI, title, or PAP-key
    existing_key = find_existing_match(metadata, doi_to_key, title_to_key, existing_keys)
    if existing_key:
        print(f"  MATCH: Already in BibTeX as '{existing_key}'")
        manifest['processed'][filename] = {
            'bibtex_key': existing_key,
            'paper_yaml': f"PAP-{existing_key}.yaml",
            'title': metadata.get('title', '')[:80],
            'date': datetime.now().strftime('%Y-%m-%d'),
            'doi': metadata.get('doi', ''),
            'match_type': 'doi_or_title',
            'action': 'linked_existing',
        }
        # Create YAML if it doesn't exist yet
        paper_yaml_id = f"PAP-{existing_key}"
        yaml_path = PAPER_REF_DIR / f"{paper_yaml_id}.yaml"
        if not yaml_path.exists() and not dry_run:
            yaml_data = create_paper_yaml(existing_key, metadata)
            with open(yaml_path, 'w', encoding='utf-8') as f:
                yaml.dump(yaml_data, f, default_flow_style=False, allow_unicode=True, sort_keys=False)
            existing_yamls.add(paper_yaml_id)
            print(f"  → Created missing YAML: {paper_yaml_id}.yaml")
        return 'linked', existing_key, metadata

    # Generate BibTeX key — prefer filename-derived key if validated against content
    fn_key, fn_source = extract_key_from_filename(filepath, existing_keys)
    if fn_key and fn_key not in existing_keys:
        valid, reason = validate_filename_key(fn_key, fn_source, metadata)
        if valid:
            key = fn_key
            print(f"  KEY from filename ({fn_source}, {reason}): {key}")
        else:
            # Safety net: filename disagrees with content → fall back to content-parsing
            key = generate_bibtex_key(metadata)
            print(f"  KEY from content (filename rejected: {reason}): {key}")
    else:
        key = generate_bibtex_key(metadata)

    # Avoid key collisions
    original_key = key
    counter = 2
    while key in existing_keys:
        key = f"{original_key}_{counter}"
        counter += 1

    paper_yaml_id = f"PAP-{key}"
    if paper_yaml_id in existing_yamls:
        manifest['skipped'][filename] = {
            'reason': f"already_exists_as_{paper_yaml_id}",
            'date': datetime.now().strftime('%Y-%m-%d'),
        }
        return 'skipped', key, metadata

    if dry_run:
        return 'created', key, metadata

    # 1. Create BibTeX entry
    bib_entry = create_bibtex_entry(key, metadata)
    with open(BIB_PATH, 'a', encoding='utf-8') as f:
        f.write(f"\n\n{bib_entry}\n")
    existing_keys.add(key)

    # 2. Create Paper-YAML
    yaml_data = create_paper_yaml(key, metadata)
    yaml_path = PAPER_REF_DIR / f"PAP-{key}.yaml"
    with open(yaml_path, 'w', encoding='utf-8') as f:
        yaml.dump(yaml_data, f, default_flow_style=False, allow_unicode=True, sort_keys=False)
    existing_yamls.add(paper_yaml_id)

    # 3. Add to queue
    add_to_queue(key, metadata, queue_data)

    # 4. Update manifest
    # Track key source (filename-derived vs content-extracted)
    key_source = 'content_extracted'
    if fn_key and key == fn_key:
        key_source = fn_source  # e.g., 'bea_lab_filename', 'canonical_filename', 'pap_prefix'

    manifest['processed'][filename] = {
        'bibtex_key': key,
        'paper_yaml': f"PAP-{key}.yaml",
        'title': metadata.get('title', '')[:80],
        'date': datetime.now().strftime('%Y-%m-%d'),
        'doi': metadata.get('doi', ''),
        'action': 'created_new',
        'key_source': key_source,
    }

    return 'created', key, metadata


def get_unprocessed_files(manifest, file_type=None):
    """Get list of files not yet processed.
    Deduplicates PDF+TXT pairs: prefers TXT over PDF when both exist.
    Also skips PDF companions of already-processed TXT files (and vice versa).
    """
    processed = set(manifest.get('processed', {}).keys())
    skipped = set(manifest.get('skipped', {}).keys())
    known = processed | skipped

    # Build set of stems that are already processed (any extension)
    known_stems = set()
    for name in known:
        known_stems.add(Path(name).stem)

    # Collect all candidate files
    all_files = {}
    for f in sorted(INTAKE_DIR.iterdir()):
        if f.is_dir():
            continue
        if f.name in known:
            continue
        if should_skip(f.name):
            continue
        stem = f.stem
        ext = f.suffix.lower()
        if ext not in {'.txt', '.pdf', '.md'}:
            continue
        # Skip if any version of this stem is already processed
        if stem in known_stems:
            continue
        if stem not in all_files:
            all_files[stem] = {}
        all_files[stem][ext] = f

    # Deduplicate: prefer TXT over PDF (TXT has better metadata)
    files = []
    for stem, exts in sorted(all_files.items()):
        if file_type:
            target_ext = f'.{file_type}'
            if target_ext in exts:
                files.append(exts[target_ext])
        elif '.txt' in exts:
            files.append(exts['.txt'])
        elif '.md' in exts:
            files.append(exts['.md'])
        elif '.pdf' in exts:
            files.append(exts['.pdf'])

    return files


def show_status(manifest):
    """Show current processing status."""
    processed = manifest.get('processed', {})
    skipped = manifest.get('skipped', {})

    total_files = sum(1 for f in INTAKE_DIR.iterdir() if f.is_file())
    unprocessed = get_unprocessed_files(manifest)

    print("=" * 70)
    print("AUTO-INTAKE PIPELINE STATUS")
    print("=" * 70)
    print(f"\n  Total files in integrated/:  {total_files}")
    print(f"  Processed (in EBF DB):       {len(processed)}")
    print(f"  Skipped (not papers):        {len(skipped)}")
    print(f"  Unprocessed:                 {len(unprocessed)}")
    print()

    if unprocessed:
        # Count by type
        by_ext = {}
        for f in unprocessed:
            ext = f.suffix.lower() or '(no ext)'
            by_ext[ext] = by_ext.get(ext, 0) + 1

        print("  Unprocessed by type:")
        for ext, count in sorted(by_ext.items(), key=lambda x: -x[1]):
            print(f"    {ext:8s}  {count}")

        print(f"\n  Next 5 unprocessed:")
        for f in unprocessed[:5]:
            print(f"    {f.name[:70]}")

    print()


def main():
    parser = argparse.ArgumentParser(description='Auto-intake papers from BEA Lab uploads')
    parser.add_argument('--status', action='store_true', help='Show processing status')
    parser.add_argument('--file', type=str, help='Process a single file')
    parser.add_argument('--batch', type=int, help='Process N files')
    parser.add_argument('--all', action='store_true', help='Process all unprocessed files')
    parser.add_argument('--dry-run', action='store_true', help='Show what would be done')
    parser.add_argument('--type', type=str, choices=['txt', 'pdf', 'md'], help='Filter by file type')
    parser.add_argument('--reset-skipped', action='store_true', help='Reset skipped files for reprocessing')

    args = parser.parse_args()

    manifest = load_manifest()

    if args.reset_skipped:
        manifest['skipped'] = {}
        save_manifest(manifest)
        print("Skipped files reset.")
        return

    if args.status:
        show_status(manifest)
        return

    # Load existing data
    existing_keys, doi_to_key, title_to_key = load_existing_bib_data()
    existing_yamls = load_existing_paper_yamls()

    # Load queue
    queue_data = []

    # Determine files to process
    if args.file:
        files = [Path(args.file)]
        if not files[0].is_absolute():
            files = [ROOT / files[0]]
    elif args.batch:
        files = get_unprocessed_files(manifest, args.type)[:args.batch]
    elif args.all:
        files = get_unprocessed_files(manifest, args.type)
    else:
        parser.print_help()
        return

    if not files:
        print("No files to process.")
        return

    mode = "DRY RUN" if args.dry_run else "PROCESSING"
    print(f"\n{'=' * 70}")
    print(f"AUTO-INTAKE PIPELINE — {mode}")
    print(f"{'=' * 70}")
    print(f"Files to process: {len(files)}\n")

    success_count = 0
    linked_count = 0
    skip_count = 0
    error_count = 0

    for i, filepath in enumerate(files, 1):
        print(f"[{i}/{len(files)}] {filepath.name[:60]}")

        if not filepath.exists():
            print(f"  FILE NOT FOUND\n")
            error_count += 1
            continue

        status, key, metadata = process_file(
            filepath, existing_keys, existing_yamls, doi_to_key, title_to_key,
            manifest, queue_data, dry_run=args.dry_run
        )

        if status == 'created':
            success_count += 1
            title = metadata.get('title', '')[:50] if metadata else ''
            doi = metadata.get('doi', '') or 'no DOI'
            print(f"  → NEW: {key}")
            print(f"    Title: {title}")
            print(f"    DOI: {doi}")
            if args.dry_run:
                print(f"    [DRY RUN — would create BibTeX + YAML + Queue entry]")
        elif status == 'linked':
            linked_count += 1
            print(f"  → LINKED to existing: {key}")
        elif status == 'error':
            error_count += 1
        else:
            skip_count += 1
            if key:
                print(f"  → SKIPPED (already exists as PAP-{key})")
            else:
                print(f"  → SKIPPED")
        print()

    # Save results
    if not args.dry_run and (success_count > 0 or skip_count > 0 or linked_count > 0):
        save_manifest(manifest)

        # Append to queue file if there are new entries
        if queue_data:
            if QUEUE_PATH.exists():
                with open(QUEUE_PATH, 'r', encoding='utf-8') as f:
                    existing_queue = yaml.safe_load(f) or {}
            else:
                existing_queue = {}

            if 'papers' not in existing_queue:
                existing_queue['papers'] = []

            existing_queue['papers'].extend(queue_data)

            with open(QUEUE_PATH, 'w', encoding='utf-8') as f:
                yaml.dump(existing_queue, f, default_flow_style=False, allow_unicode=True, sort_keys=False)

    # Summary
    print(f"{'=' * 70}")
    print(f"SUMMARY")
    print(f"{'=' * 70}")
    print(f"  New papers:       {success_count}")
    print(f"  Linked existing:  {linked_count}")
    print(f"  Skipped:          {skip_count}")
    print(f"  Errors:           {error_count}")
    if not args.dry_run and success_count > 0:
        print(f"\n  Created:")
        print(f"    BibTeX entries:     {success_count} (in bcm_master.bib)")
        print(f"    Paper-YAMLs:        {success_count} (in data/paper-references/)")
        print(f"    Queue entries:      {success_count} (in paper-integration-queue.yaml)")
        print(f"    Manifest updated:   data/auto-intake-manifest.yaml")
    print()


if __name__ == '__main__':
    main()
