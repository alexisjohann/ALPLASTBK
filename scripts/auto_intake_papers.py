#!/usr/bin/env python3
"""
Auto-Intake Pipeline for BEA Lab Uploads
==========================================

Automatically processes papers uploaded via the BEA Lab frontend
(papers/evaluated/integrated/) and integrates them into the EBF database:

1. Detects new/unprocessed files (TXT, PDF)
2. Extracts metadata (title, authors, year, DOI, journal)
3. Creates BibTeX stub in bcm_master.bib
4. Creates Paper-YAML in data/paper-references/
5. Classifies integration level
6. Adds to paper-integration-queue.yaml

Usage:
    # Status: show what's unprocessed
    python scripts/auto_intake_papers.py --status

    # Process single file (Experiment-First: 1)
    python scripts/auto_intake_papers.py --file "papers/evaluated/integrated/file.txt"

    # Process batch (Experiment-First: 10)
    python scripts/auto_intake_papers.py --batch 10

    # Process all unprocessed files
    python scripts/auto_intake_papers.py --all

    # Dry run (no changes)
    python scripts/auto_intake_papers.py --all --dry-run

    # Filter by type
    python scripts/auto_intake_papers.py --all --type txt
    python scripts/auto_intake_papers.py --all --type pdf

Author: EBF Team
Version: 1.0.0
Date: February 2026
"""

