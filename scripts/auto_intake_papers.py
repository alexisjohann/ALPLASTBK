# File types to skip (not papers)
SKIP_PATTERNS = [
    r"^_.*TEST.*",           # Test files
    r"^Duplikat-Test",       # Duplicate tests
    r"^EBF:",                # EBF system queries
    r"^_PUSH_TEST",          # Push tests
    r"^_BEA_LAB_TEST",       # BEA Lab tests
    r"\.xlsx$",              # Spreadsheets
    r"\.docx$",              # Word docs (not papers)
    r"^Long List",           # Lists
    r"^5_Lernfelder",        # Meta-documents
]

# Known non-paper extensions
NON_PAPER_EXT = {'.xlsx', '.docx', '.pptx', '.zip', '.csv'}

# BEA Lab canonical filename pattern: PAP_Author_Year_Title-Words.ext
# Example: PAP_Kahneman_1979_Prospect-Theory-Analysis-Decision-Under.pdf
BEA_LAB_PATTERN = re.compile(
    r'^(?P<doctype>PAP|BOOK|STU|SSRN)_(?P<author>[A-Za-z]+)_(?P<year>\d{4})_(?P<titleslug>.+)$'
)


def parse_bea_lab_filename(filepath):
    """Parse BEA Lab canonical filename into metadata components.

    Recognizes filenames generated by the BEA Lab frontend:
        PAP_Kahneman_1979_Prospect-Theory-Analysis-Decision-Under.pdf
        BOOK_Thaler_2015_Misbehaving-Science-Behavioral-Economics.pdf
        STU_McKinsey_2024_Global-Survey-Results.pdf

    Returns dict with author, year, title_slug, doc_type — or None if not BEA Lab format.
    """
    stem = filepath.stem
    m = BEA_LAB_PATTERN.match(stem)
    if not m:
        return None

    title_slug = m.group('titleslug').replace('-', ' ')
    return {
        'doc_type': m.group('doctype'),
        'author': m.group('author'),
        'year': m.group('year'),
        'title_slug': title_slug,
    }


def extract_key_from_filename(filepath, existing_keys=None):
    """Try to derive the canonical BibTeX key directly from the filename.

    Three strategies (in priority order):
      1. PAP-{key}.ext → key directly (existing SSOT convention)
      2. BEA Lab format PAP_Author_Year_Title.ext → generate_canonical_key()
      3. Filename IS a canonical key: kahneman1979prospect.ext → use directly

    Returns (bibtex_key, source_hint) or (None, None).
    """
    stem = filepath.stem

    # Strategy 1: PAP-prefix (existing convention in repo)
    if stem.startswith('PAP-'):
        candidate = stem[4:]  # Strip "PAP-"
        if existing_keys is None or candidate in existing_keys:
            return candidate, 'pap_prefix'
        # Fuzzy: case-insensitive
        if existing_keys:
            for ek in existing_keys:
                if ek.lower() == candidate.lower():
                    return ek, 'pap_prefix_fuzzy'

    # Strategy 2: BEA Lab format (PAP_Author_Year_Title-Slug.ext)
    bea = parse_bea_lab_filename(filepath)
    if bea:
        key = _generate_canonical_key(bea['author'], bea['year'], bea['title_slug'])
        return key, 'bea_lab_filename'

    # Strategy 3: Filename is already a canonical key (kahneman1979prospect.ext)
    if _is_canonical(stem):
        return stem, 'canonical_filename'

    return None, None


def validate_filename_key(fn_key, fn_source, metadata):
    """Cross-validate filename-derived key against content-extracted metadata.

    Safety net: if BEA Lab's extract_paper_metadata() produced wrong metadata
    (e.g. "Vol" instead of "Kahneman"), the filename-key will be wrong.
    We catch this by comparing the author in the filename against the author
    extracted from file content.

    Returns:
        (True, reason)  if filename key is trustworthy
        (False, reason) if content disagrees — fall back to content-parsing
    """
    # PAP-prefix and canonical filenames are already validated keys
    if fn_source in ('pap_prefix', 'pap_prefix_fuzzy', 'canonical_filename'):
        return True, 'trusted_format'

    # For BEA Lab filenames, cross-check author
    if fn_source == 'bea_lab_filename':
        content_authors = metadata.get('authors', [])
        if not content_authors:
            # No content author to compare — trust filename
            return True, 'no_content_author'

        # Extract author component from the filename key
        # fn_key format: "kahneman1979prospect"
        m = re.match(r'^([a-z]+)\d{4}', fn_key)
        if not m:
            return False, 'key_parse_failed'

        fn_author = m.group(1)  # e.g. "kahneman"

        # Extract last name from first content author
        first_content_author = content_authors[0]
        if ',' in first_content_author:
            content_lastname = first_content_author.split(',')[0].strip()
        else:
            parts = first_content_author.strip().split()
            content_lastname = parts[-1] if parts else ''

        from bibtex_key_generator import normalize_to_ascii
        content_lastname = normalize_to_ascii(content_lastname).lower()
        content_lastname = re.sub(r'[^a-z]', '', content_lastname)

        if not content_lastname:
            return True, 'no_content_lastname'

        # Compare: exact match or one is prefix of other (handles "Della" vs "DellaVigna")
        if fn_author == content_lastname:
            return True, 'author_match'
        if fn_author.startswith(content_lastname) or content_lastname.startswith(fn_author):
            return True, 'author_prefix_match'

        # Mismatch — content disagrees with filename
        return False, f'author_mismatch:filename={fn_author},content={content_lastname}'

    return True, 'unknown_source'


def load_manifest():
    """Load or create the auto-intake manifest tracking processed files."""
    if MANIFEST_PATH.exists():
        with open(MANIFEST_PATH, 'r', encoding='utf-8') as f:
            data = yaml.safe_load(f) or {}
    else:
        data = {}

    if 'processed' not in data:
        data['processed'] = {}
    if 'skipped' not in data:
        data['skipped'] = {}
    if 'metadata' not in data:
        data['metadata'] = {
            'created': datetime.now().strftime('%Y-%m-%d'),
            'version': '1.0',
            'description': 'Tracks files processed by auto_intake_papers.py'
        }
    return data


def save_manifest(data):
    """Save the manifest."""
    data['metadata']['last_updated'] = datetime.now().strftime('%Y-%m-%dT%H:%M:%S')
    with open(MANIFEST_PATH, 'w', encoding='utf-8') as f:
        yaml.dump(data, f, default_flow_style=False, allow_unicode=True, sort_keys=False)


def should_skip(filename):
    """Check if a file should be skipped (not a paper)."""
    for pattern in SKIP_PATTERNS:
        if re.search(pattern, filename, re.IGNORECASE):
            return True
    ext = Path(filename).suffix.lower()
    if ext in NON_PAPER_EXT:
        return True
    if ext not in {'.txt', '.pdf', '.md'}:
        return True
    return False


def load_existing_bib_data():
    """Load all existing BibTeX keys, DOIs, and titles from bcm_master.bib.
    Returns: (keys_set, doi_to_key_dict, titles_to_key_dict)
    """
    keys = set()
    doi_to_key = {}
    title_to_key = {}

    if BIB_PATH.exists():
        with open(BIB_PATH, 'r', encoding='utf-8') as f:
            content = f.read()

        # Parse entries
        current_key = None
        current_doi = None
        current_title = None

        for line in content.split('\n'):
            m = re.match(r'@\w+\s*\{\s*([^,]+)\s*,', line)
            if m:
                # Save previous entry
                if current_key:
                    if current_doi:
                        doi_to_key[current_doi.lower().strip()] = current_key
                    if current_title:
                        # Normalize title for fuzzy matching
                        norm_title = re.sub(r'[^a-z0-9\s]', '', current_title.lower()).strip()
                        norm_title = re.sub(r'\s+', ' ', norm_title)
                        title_to_key[norm_title] = current_key
                keys.add(m.group(1).strip())
                current_key = m.group(1).strip()
                current_doi = None
                current_title = None
            elif current_key:
                doi_match = re.match(r'\s*doi\s*=\s*\{([^}]+)\}', line, re.IGNORECASE)
                if doi_match:
                    current_doi = doi_match.group(1)
                title_match = re.match(r'\s*title\s*=\s*\{(.+?)(?:\}|$)', line, re.IGNORECASE)
                if title_match:
                    current_title = title_match.group(1)

        # Don't forget last entry
        if current_key:
            if current_doi:
                doi_to_key[current_doi.lower().strip()] = current_key
            if current_title:
                norm_title = re.sub(r'[^a-z0-9\s]', '', current_title.lower()).strip()
                norm_title = re.sub(r'\s+', ' ', norm_title)
                title_to_key[norm_title] = current_key

    return keys, doi_to_key, title_to_key


def find_existing_match(metadata, doi_to_key, title_to_key, existing_keys=None):
    """Check if a paper already exists in BibTeX by DOI, title, or PAP-key match.
    Returns: existing_key or None
    """
    # 0. Filename-based key extraction (PAP-prefix, BEA Lab format, or canonical key)
    source_file = metadata.get('source_file', '')
    if source_file:
        filepath = Path(source_file)
        fn_key, fn_source = extract_key_from_filename(filepath, existing_keys)
        if fn_key and existing_keys:
            # Direct match
            if fn_key in existing_keys:
                metadata['_filename_key'] = fn_key
                metadata['_filename_key_source'] = fn_source
                return fn_key
            # Case-insensitive match
            for ek in existing_keys:
                if ek.lower() == fn_key.lower():
                    metadata['_filename_key'] = ek
                    metadata['_filename_key_source'] = fn_source
                    return ek

    # 1. DOI match (exact)
    doi = metadata.get('doi', '')
    if doi:
        doi_lower = doi.lower().strip()
        if doi_lower in doi_to_key:
            return doi_to_key[doi_lower]

    # 2. Title match (normalized)
    title = metadata.get('title', '')
    if title:
        norm_title = re.sub(r'[^a-z0-9\s]', '', title.lower()).strip()
        norm_title = re.sub(r'\s+', ' ', norm_title)
        if norm_title in title_to_key:
            return title_to_key[norm_title]

        # 3. Fuzzy title match (first 40 chars)
        if len(norm_title) > 30:
            prefix = norm_title[:40]
            for existing_title, key in title_to_key.items():
                if existing_title.startswith(prefix):
                    return key

    return None


